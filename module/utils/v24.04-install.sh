####
# Useful utility packages to have on the machine.
####
# curl   - (see a URL) curl is a tool for transferring data from or to a server using URLs.
# wget   - (web get)  Wget is a free utility for non-interactive download of files from the Web.
# git    - version control
# tree   - list contents of directories in a tree-like format.
# htop   - (human top) - human friendly version of top (see the top processes)
# zip    - package and compress (archive) files
# unzip  - list, test and extract compressed files in a ZIP archive
# ncdu   - ncdu (NCurses Disk Usage) is a curses-based version of the well-known 'du', and provides a fast way to see what directories are using your disk space.
# jq     - command-line JSON processor
sudo apt-get install -y curl wget git tree htop zip unzip ncdu jq
